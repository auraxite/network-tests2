Задача: Измерение чистой задержки выполнения ядра на GPU.

Запускает пустое или минимальное ядро (__global__ void null_kernel()).
Измеряет время запуска и синхронизации (cudaEventElapsedTime).
Выявляет накладные расходы драйвера и CUDA runtime.
Используется для оценки минимальной возможной задержки GPU.

Выходные данные:
- Среднее время запуска (kernel launch latency).
- Вариации при повторных вызовах.