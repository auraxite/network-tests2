Задача: Измерение пропускной способности (bandwidth) между устройствами и хостом.

Вычисляет скорость передачи данных при cudaMemcpy:
Host → Device (H2D)
Device → Host (D2H)
Device → Device (D2D, если P2P разрешено)

Использует таймеры CUDA Events (cudaEventRecord) для измерения реального времени.
Поддерживает выбор размера буфера и количества итераций.

Выходные данные:
- Средняя и пиковая пропускная способность (в ГБ/с).
- .nc-совместимый вывод.