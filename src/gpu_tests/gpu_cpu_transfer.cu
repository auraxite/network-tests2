Задача: Измерение задержек копирования между CPU и GPU-памятью (H2D / D2H latency).

Тестирует мелкие передачи (cudaMemcpy блоками 4–64 байта).
Определяет зависимость latency от размера блока.
Сравнивает pinned и pageable память (cudaHostAlloc vs обычный malloc).
Может использовать cudaMemcpyAsync для анализа асинхронности.

Выходные данные:
- Латентность (в микросекундах) на разных размерах буфера.
- График "размер блока → задержка".